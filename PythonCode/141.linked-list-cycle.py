# @lc code=start
# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution:
    def hasCycle(self, head: Optional[ListNode]) -> bool:
        if not head or not head.next:
            return False
        quick_pt = head.next
        slow_pt = head
        while quick_pt != slow_pt:
            if not quick_pt or not quick_pt.next:
                return False
            quick_pt, slow_pt = quick_pt.next.next, slow_pt.next
        return True

        if not head or not head.next:
            return False
        
        slow = head
        fast = head.next

        while slow != fast:
            if not fast or not fast.next:
                return False
            slow = slow.next
            fast = fast.next.next
        
        return True



        
